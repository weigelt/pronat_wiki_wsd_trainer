package edu.kit.ipd.parse.wiki_wsd.trainer;

import java.io.Serial;

public class RandomNullPointerException extends Exception {

	@Serial
	private static final long serialVersionUID = -1545260236985256671L;

	public RandomNullPointerException() {
	}

	public RandomNullPointerException(String message) {
		super(message);
	}

	public RandomNullPointerException(Throwable cause) {
		super(cause);
	}

	public RandomNullPointerException(String message, Throwable cause) {
		super(message, cause);
	}

	public RandomNullPointerException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
